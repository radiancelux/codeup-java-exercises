public class StringExercise {
    public static void main(String[] args) {
        // Output examples
        String example1 = "We don't need no education";
        String example2 = "We don't need no thought control";
        String example3 = "Check \"this\" out!, \"s inside of \"s!";
        String example4 = "In windows, the main drive is usually C:\\";
        String example5 = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";

        // Print examples
        System.out.println(example1);
        System.out.println(example2);
        System.out.println(example3);
        System.out.println(example4);
        System.out.println(example5);
    }
}
